#Program for demobstarting the functionality of Relational Op
#RelationalOpEx1.py
a=float(input("Enter First Value:"))
b=float(input("Enter Second Value:"))
print("-"*50)
print("\tResults of Relational Operators")
print("-"*50)
print("\t\t{} > {} = {}".format(a,b,a>b))
print("\t\t{} < {} = {}".format(a,b,a<b))
print("\t\t{} =={} = {}".format(a,b,a==b))
print("\t\t{} !={} = {}".format(a,b,a!=b))
print("\t\t{} >= {} = {}".format(a,b,a>=b))
print("\t\t{} <= {} = {}".format(a,b,a<=b))
print("-"*50)