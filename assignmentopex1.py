a,b=input("Enter Value of a:"),input("Enter Value of b:")
print("------------------------------------")
print("\tOriginal Value of a={}".format(a))
print("\tOriginal Value of b={}".format(b))
print("------------------------------------")
a,b=b,a # Multi Line assignment
